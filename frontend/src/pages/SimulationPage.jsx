/**
 * SimulationPage.jsx
 * Reads simulation results from PipelineContext.
 * Shows a CTA to run the pipeline if no data is available yet.
 */
import React from "react";
import { Link } from "react-router-dom";
import { usePipeline } from "../context/PipelineContext";
import FloodMap from "../components/map/FloodMap";
import SimulationChart from "../components/map/SimulationChart";
import StatCard from "../components/prediction/StatCard";

const INDIA_CENTER = [20.5937, 78.9629];

export default function SimulationPage() {
  const { simulation, running, prediction } = usePipeline();

  const mapCenter = simulation?.geojson
    ? [simulation.location?.lat ?? INDIA_CENTER[0], simulation.location?.lon ?? INDIA_CENTER[1]]
    : INDIA_CENTER;

  const timeline = simulation?.timeline_chart || [];
  const impacts  = simulation?.impact_summary  || [];

  return (
    <div>
      <div className="page-header">
        <h1 className="page-title">🗺️ Flood Simulation</h1>
        <p className="page-subtitle">
          Hydrological flood scenario modelling — results generated by the AI pipeline
        </p>
      </div>

      {/* Running banner */}
      {running && (
        <div className="alert alert-info mb-6" style={{ display: "flex", alignItems: "center", gap: "0.75rem" }}>
          <span className="spinner spinner-sm" />
          <div className="alert-content">
            <div className="alert-message">Pipeline is running — simulation results will appear here automatically.</div>
          </div>
        </div>
      )}

      {/* Empty state */}
      {!simulation && !running && (
        <div className="card" style={{ textAlign: "center", padding: "4rem 2rem" }}>
          <div style={{ fontSize: "3.5rem", marginBottom: "1rem" }}>🗺️</div>
          <div style={{ fontWeight: 700, fontSize: "1.1rem", marginBottom: "0.5rem" }}>
            No Simulation Data Yet
          </div>
          <p style={{ color: "var(--text-muted)", fontSize: "0.875rem", marginBottom: "1.5rem", maxWidth: 380, margin: "0 auto 1.5rem" }}>
            Run the prediction pipeline first — the simulation agent will model flood inundation scenarios automatically.
          </p>
          <Link to="/predict" className="btn btn-primary">
            🚀 Go to Prediction →
          </Link>
        </div>
      )}

      {/* Results */}
      {simulation && (
        <div style={{ display: "flex", flexDirection: "column", gap: "1.5rem" }}>

          {/* Context header */}
          {prediction && (
            <div className="card" style={{ borderLeft: "4px solid var(--accent)", padding: "1rem 1.25rem" }}>
              <div style={{ display: "flex", alignItems: "center", gap: "1rem", flexWrap: "wrap" }}>
                <div style={{ fontSize: "0.75rem", color: "var(--text-muted)", fontWeight: 600, letterSpacing: "0.05em" }}>
                  BASED ON PIPELINE RUN
                </div>
                {prediction.risk_level && (
                  <span className="badge badge-primary">{prediction.risk_level.toUpperCase()} RISK</span>
                )}
                <Link to="/predict" style={{ marginLeft: "auto", fontSize: "0.75rem", color: "var(--primary)" }}>
                  ← Back to Prediction
                </Link>
              </div>
            </div>
          )}

          {/* Key Stats */}
          <div className="grid-2" style={{ gap: "1rem" }}>
            {[
              { label: "Flood Risk Score", value: simulation.flood_risk_score != null ? simulation.flood_risk_score.toFixed(2) : "—", icon: "🌊", color: "var(--risk-high)" },
              { label: "Affected Area",    value: simulation.affected_area_km2 != null ? `${simulation.affected_area_km2} km²` : "—", icon: "📐", color: "var(--risk-medium)" },
              { label: "Peak Discharge",   value: simulation.peak_discharge_m3s != null ? `${simulation.peak_discharge_m3s} m³/s` : "—", icon: "💧", color: "var(--accent)" },
              { label: "Peak Depth",       value: simulation.peak_depth_m != null ? `${simulation.peak_depth_m.toFixed(1)} m` : "—", icon: "📊", color: "var(--primary)" },
            ].map(({ label, value, icon, color }) => (
              <StatCard
                key={label}
                icon={icon}
                label={label}
                value={value}
                iconBg={color + "22"}
                iconColor={color}
              />
            ))}
          </div>

          {/* Inundation Map */}
          <div className="card" style={{ padding: 0, overflow: "hidden" }}>
            <div style={{ padding: "0.875rem 1.25rem", borderBottom: "1px solid var(--border)", display: "flex", alignItems: "center", gap: "0.5rem" }}>
              <span className="card-title">🗺️ Inundation Map</span>
              {simulation.scenario_name && (
                <span className="badge badge-primary">{simulation.scenario_name}</span>
              )}
            </div>
            <FloodMap
              geojson={simulation.geojson}
              center={mapCenter}
              zoom={simulation.geojson ? 8 : 5}
              height={380}
            />
          </div>

          {/* Timeline chart */}
          <SimulationChart timeline={timeline} peakHour={simulation.peak_hour} />

          {/* Impact summary */}
          {impacts.slice(0, 4).length > 0 && (
            <div className="grid-2" style={{ gap: "1rem" }}>
              {impacts.slice(0, 4).map((imp) => (
                <StatCard
                  key={imp.metric}
                  icon="📊"
                  label={imp.metric}
                  value={`${imp.value.toLocaleString("en-IN")} ${imp.unit}`}
                  sub={imp.description}
                  iconBg="var(--accent-dim)"
                  iconColor="var(--accent)"
                />
              ))}
            </div>
          )}

          {/* AI Summary */}
          {simulation.summary && (
            <div className="alert alert-info">
              <span className="alert-icon">🤖</span>
              <div className="alert-content">
                <div className="alert-title">AI Summary</div>
                <div className="alert-message">{simulation.summary}</div>
              </div>
            </div>
          )}

          {/* Navigation */}
          <div style={{ display: "flex", gap: "0.75rem", flexWrap: "wrap" }}>
            <Link to="/recommendations" className="btn btn-secondary" style={{ fontSize: "0.8rem" }}>
              💡 View Recommendations →
            </Link>
            <Link to="/alerting" className="btn btn-secondary" style={{ fontSize: "0.8rem" }}>
              🔔 View Alerting →
            </Link>
          </div>
        </div>
      )}
    </div>
  );
}
