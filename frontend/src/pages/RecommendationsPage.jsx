/**
 * RecommendationsPage.jsx
 * Reads recommendation results from PipelineContext.
 * Shows a CTA to run the pipeline if no data is available yet.
 */
import React from "react";
import { Link } from "react-router-dom";
import { usePipeline } from "../context/PipelineContext";
import RecommendationList from "../components/recommendation/RecommendationList";

export default function RecommendationsPage() {
  const { recommendations, recSummary, recSafetyMsg, running, done, prediction } = usePipeline();

  const hasData = (recommendations && recommendations.length > 0) || recSummary;
  const riskColor = {
    HIGH: "var(--risk-high)", MEDIUM: "var(--risk-medium)",
    LOW: "var(--risk-low)",   CRITICAL: "var(--risk-critical)",
  }[prediction?.risk_level?.toUpperCase()] || "var(--primary)";

  return (
    <div>
      <div className="page-header">
        <h1 className="page-title">💡 Recommendations</h1>
        <p className="page-subtitle">
          AI-powered, context-aware flood safety guidance generated by the pipeline
        </p>
      </div>

      {/* Pipeline status banner */}
      {running && (
        <div className="alert alert-info mb-6" style={{ display: "flex", alignItems: "center", gap: "0.75rem" }}>
          <span className="spinner spinner-sm" />
          <div className="alert-content">
            <div className="alert-message">Pipeline is running — recommendations will appear here automatically when ready.</div>
          </div>
        </div>
      )}

      {/* Empty state — no pipeline run yet */}
      {!hasData && !running && (
        <div className="card" style={{ textAlign: "center", padding: "4rem 2rem" }}>
          <div style={{ fontSize: "3.5rem", marginBottom: "1rem" }}>💡</div>
          <div style={{ fontWeight: 700, fontSize: "1.1rem", marginBottom: "0.5rem" }}>
            No Recommendations Yet
          </div>
          <p style={{ color: "var(--text-muted)", fontSize: "0.875rem", marginBottom: "1.5rem", maxWidth: 380, margin: "0 auto 1.5rem" }}>
            Run the prediction pipeline first — Gemini will automatically generate safety recommendations based on flood risk.
          </p>
          <Link to="/predict" className="btn btn-primary">
            🚀 Go to Prediction →
          </Link>
        </div>
      )}

      {/* Results */}
      {hasData && (
        <div style={{ display: "flex", flexDirection: "column", gap: "1.5rem" }}>

          {/* Context header */}
          {prediction && (
            <div className="card" style={{ borderLeft: `4px solid ${riskColor}`, padding: "1rem 1.25rem" }}>
              <div style={{ display: "flex", alignItems: "center", gap: "1rem", flexWrap: "wrap" }}>
                <div style={{ fontSize: "0.75rem", color: "var(--text-muted)", fontWeight: 600, letterSpacing: "0.05em" }}>
                  BASED ON PIPELINE RUN
                </div>
                {prediction.risk_level && (
                  <span className="badge" style={{
                    background: riskColor + "22", color: riskColor,
                    border: `1px solid ${riskColor}44`, fontWeight: 700, fontSize: "0.7rem",
                  }}>
                    {prediction.risk_level.toUpperCase()} RISK
                  </span>
                )}
                {prediction.flood_probability != null && (
                  <span style={{ color: "var(--text-muted)", fontSize: "0.8rem" }}>
                    {Math.round(prediction.flood_probability * 100)}% flood probability
                  </span>
                )}
                <Link to="/predict" style={{ marginLeft: "auto", fontSize: "0.75rem", color: "var(--primary)" }}>
                  ← Back to Prediction
                </Link>
              </div>
            </div>
          )}

          {/* Recommendation list */}
          <div className="card animate-fade">
            <div className="card-title mb-4">💡 Safety Recommendations</div>
            <RecommendationList
              recommendations={recommendations || []}
              summary={recSummary}
              safetyMessage={recSafetyMsg}
            />
          </div>

          {/* Navigate to next agents */}
          <div style={{ display: "flex", gap: "0.75rem", flexWrap: "wrap" }}>
            <Link to="/simulation" className="btn btn-secondary" style={{ fontSize: "0.8rem" }}>
              🗺️ View Simulation →
            </Link>
            <Link to="/alerting" className="btn btn-secondary" style={{ fontSize: "0.8rem" }}>
              🔔 View Alerting →
            </Link>
          </div>
        </div>
      )}
    </div>
  );
}
